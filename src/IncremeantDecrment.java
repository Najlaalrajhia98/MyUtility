public class IncremeantDecrment {

    /** this class is used to genrate incremeant and decreament seiers of number using for loops.
     * @param args
     */
    public static void main(String[] args) {

        int j=0;
        for (int i=0; i<=4 ; i++) 
        {
            System.out.println(i);
        }
        for (j=4;j>=0 ;j--) 
        {
            System.out.println(j);

        }
    }

}
